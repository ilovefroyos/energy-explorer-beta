        <h3 class="line">
            Where does energy come from in British Columbia?
        </h3>
        <div class="text">
            Much of the energy currently used in Metro Vancouver homes comes from remote suppliers in BC and Alberta, at least 400-1000 kilometers away. Natural gas to heat our homes is piped from northeastern BC and Alberta. Electricity to power our homes comes primarily (approximately 90%) from Hydroelectric dams in southeastern and central BC.  This hydroelectricity is supplemented by natural gas electricity generation in BC and, at times, by other fossil fuel-based generating plants in Alberta and the United States. <br><br>
       Metro Vancouver is also crossed by railways and pipelines which ship coal and oil from the oilsands for export.  A small portion of the oil that is exported is imported back to Canada to meet our transportation needs.</div>
